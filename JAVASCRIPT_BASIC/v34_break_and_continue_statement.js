document.write('<b>break statement <b> <br>');
// Break statement
for(i=1; i<=5; i++)
    {   
        if(i==3)
        {
            break; // stop loop 
        }
        document.write( i + '<br>'); 
    }
    
document.write('<br><b>Continue statement <b> <br>');
// Continue statement
for(i=1; i<=5; i++)
    {   
        if(i==3)
        {
            continue; // skip loop
        }
        document.write( i + '<br>'); 
    } 
    



































